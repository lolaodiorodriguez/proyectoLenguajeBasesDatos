    public void Mostrar(){
       try {
               DefaultTableModel model = new DefaultTableModel();

            // Crear modelo de tabla
            model.addColumn("ID Suministro");
            model.addColumn("Nombre");
            model.addColumn("Descripción");
            model.addColumn("Cantidad");
            model.addColumn("Precio Unitario");
            model.addColumn("ID Proveedor");
            model.addColumn("ID Hotel");
            
            // Obtener los proveedores y agregar filas
            List<Suministro> suministro = suministroDAO.obtenerSuministros();
            for (Suministro suministros : suministro) {
                model.addRow(new Object[]{
                    suministros.getIdSuministro(),
                    suministros.getNombre(),
                    suministros.getDescripcion(),
                    suministros.getCantidad(),
                    suministros.getPrecioUnitario(),
                    suministros.getIdProveedor(),
                    suministros.getIdHotel()
                });
            }
            
            // Asignar el modelo a la tabla
            jTable1.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(VistaSuministro.class.getName()).log(Level.SEVERE, null, ex);
        }
}
public void actualizarComboBoxConModelo() {
    try {
        // Obtener la lista de hoteles
        List<String> hotels = hoteles.obtenerHoteles();

        // Crear un modelo para el JComboBox
   
        // Agregar los elementos al modelo
        for (int i = 0; i < hotels.size(); i++) {
           jComboBox2.addItem( hotels.get(i));
  }

        // Asignar el modelo al JComboBox
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al cargar hoteles: " + e.getMessage(),
                                      "Error", JOptionPane.ERROR_MESSAGE);
    }
}
