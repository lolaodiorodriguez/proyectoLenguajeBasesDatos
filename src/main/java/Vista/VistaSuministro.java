private void crearSuministro() {
        try {
            int idho=hoteles.obtenerIdHotelPorNombre((String)jComboBox2.getSelectedItem());
            int idproveed=proveedorDAO.obtenerIdProveedorPorNombre((String)jComboBox1.getSelectedItem());
            Suministro suministro = new Suministro(
                    0, jTextField1.getText(), jTextField2.getText(),
                    Integer.parseInt(jTextField3.getText()), Double.parseDouble(jTextField4.getText()),
                    idproveed, idho
            );
            suministroDAO.crearSuministro(suministro);
            Mostrar();
            // Actualiza la lista después de crear
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
private void modificarSuministro() {
    
        int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada != -1) {
        try {
             int idho=hoteles.obtenerIdHotelPorNombre((String)jComboBox2.getSelectedItem());
            int idproveed=proveedorDAO.obtenerIdProveedorPorNombre((String)jComboBox1.getSelectedItem());
            Object objRestaurante = model.getValueAt(filaSeleccionada, 0);  // 0 es el índice de la primera columna
            int idSuministro = Integer.parseInt(objRestaurante.toString());
            Suministro suministro = new Suministro(
                    Integer.parseInt(jTextField3.getText()), jTextField1.getText(), jTextField2.getText(),
                    Integer.parseInt(jTextField3.getText()), Double.parseDouble(jTextField4.getText()),
                    idproveed, idho
            );
            suministroDAO.modificarSuministro(suministro, idSuministro);
            Mostrar(); // Actualiza la lista después de modificar
        } catch (Exception e) {
            e.printStackTrace();
        }
        }else {
    JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna ID DE LA TABLA.");
}
    }
