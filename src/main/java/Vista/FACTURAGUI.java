    public void Mostrar(){
          DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID FACTURA");
    model.addColumn("ID RESERVA");
    model.addColumn("FECHA EMISION");
    model.addColumn("MONTO_TOTAL");
    jTable1.setModel(model);
    
    
        List<Factura> facturas = facturaDAO.obtenerFacturas();
         for (Factura facturass : facturas) {
            model.addRow(new Object[]{
                facturass.getIdFactura(),
                facturass.getIdReserva(),
                facturass.getFechaEmision(),
                facturass.getMontoTotal()
            });
        
         }
    }
    

